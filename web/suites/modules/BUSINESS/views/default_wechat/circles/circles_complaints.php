<script>
	
	$(".icon-not_praise").click(function () {
		//$(".zan").removeClass("zan1");
		$(this).toggleClass("icon-already_praised1");
		$(this).toggleClass("bounceIn");/*bounceIn*/
		var classname=$(this).attr("class");
		//alert(classname);
		var zan_num=parseInt($('>span',this).text());
		//alert(zan_num);
		if(classname == "icon-not_praise icon-already_praised1 bounceIn"){
			zan_num +=1;
			//alert(zan_num);
			$('>span',this).text(zan_num);
			
		}else if(classname== "icon-not_praise"){
			zan_num -=1;
			//alert(zan_num);
			$('>span',this).text(zan_num);
		}
	})
</script>
